num=int(input("Enter the number : "))

match num :
		case 1:
			print("ONE")
		case 2:
			print("TWO")
		case 3:
			print("THREE")
		case 4:
			print("Four")
		case 5:
			print("FIVE")
		case 6:
			print("SIX")
		case 7:
			print("SEVEN")
		case 8:
			print("EIGHT")
		case 9:
			print("NINE")
		case _:
			print("Invalid Input")